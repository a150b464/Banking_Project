package sorting;

public class Car {
private int model;
private int price;

	public Car(int model, int price) {
	super();
	this.model = model;
	this.price = price;
}

	public int getModel() {
		return model;
	}

	public void setModel(int model) {
		this.model = model;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	
}
